def main() -> None:
    args = build_argparser().parse_args()
    if args.frame_stride < 1:
        raise ValueError("--frame-stride must be >= 1")
    if args.max_demos_per_task < 0:
        raise ValueError("--max-demos-per-task must be >= 0")
    if args.bbox_batch_size < 1:
        raise ValueError("--bbox-batch-size must be a positive integer")
    if args.grounded_sam2_chunk_size < 0:
        raise ValueError("--grounded-sam2-chunk-size must be >= 0")
    if args.vis_num_demos_per_task < 0:
        raise ValueError("--vis-num-demos-per-task must be >= 0")
    if args.vis_fps < 1:
        raise ValueError("--vis-fps must be a positive integer")
    if args.vis_max_frames < 0:
        raise ValueError("--vis-max-frames must be >= 0")
    args.device = resolve_runtime_device(args.device)

    data_root = resolve_data_root(args.data_root)
    output_root = Path(args.output_root)
    output_root.mkdir(parents=True, exist_ok=True)
    visualization_root = Path(args.vis_output_dir) if args.vis_output_dir else output_root / "visualizations"
    manifest_path = output_root / "manifest.jsonl"
    suite_roots = discover_suite_roots(data_root, args.suites)
    episodes = iter_episodes(
        suite_roots,
        episode_indices=set(args.episode_indices) if args.episode_indices else None,
        episode_start=args.episode_start,
        episode_end=args.episode_end,
        max_demos_per_task=args.max_demos_per_task or None,
        max_episodes=args.max_episodes,
    )
    if not episodes:
        raise RuntimeError("No episodes selected.")

    print(f"[BBOX] data_root={data_root}")
    print(f"[BBOX] output_root={output_root}")
    print(f"[BBOX] suites={[p.name for p in suite_roots]}")
    print(f"[BBOX] selected_episodes={len(episodes)}")
    print(f"[BBOX] max_demos_per_task={args.max_demos_per_task or 'all'}")
    print(f"[BBOX] backend={args.bbox_backend}")
    print(f"[BBOX] local_grounding_dino_config={Path(args.grounding_dino_config).resolve()}")
    print(f"[BBOX] local_grounding_dino_checkpoint={Path(args.grounding_dino_checkpoint).resolve()}")
    print(f"[BBOX] device={args.device}")
    print(f"[BBOX] visualizations: first {args.vis_num_demos_per_task} demos/task -> {visualization_root}")

    annotator_kwargs = dict(
        config_path=Path(args.grounding_dino_config),
        checkpoint_path=Path(args.grounding_dino_checkpoint),
        device=args.device,
        batch_size=args.bbox_batch_size,
        box_threshold=args.box_threshold,
        text_threshold=args.text_threshold,
        agentview_camera_keys=set(args.agentview_camera_keys),
        allow_prompt_fallback=args.allow_prompt_fallback,
    )
    if args.bbox_backend == "grounded_sam2":
        print(f"[BBOX] sam2_repo={Path(args.sam2_repo_dir).resolve()}")
        print(f"[BBOX] sam2_checkpoint={Path(args.sam2_checkpoint).resolve()}")
        bbox_annotator = GroundedSam2VideoAnnotator(
            sam2_repo_dir=Path(args.sam2_repo_dir),
            sam2_config=args.sam2_config,
            sam2_checkpoint=Path(args.sam2_checkpoint),
            chunk_size=args.grounded_sam2_chunk_size,
            offload_video_to_cpu=args.sam2_offload_video_to_cpu,
            offload_state_to_cpu=args.sam2_offload_state_to_cpu,
            **annotator_kwargs,
        )
    else:
        bbox_annotator = LocalGroundingDinoBBoxAnnotator(**annotator_kwargs)

    visualization_counts: dict[tuple[str, str], int] = {}
    for ep in tqdm(episodes, desc="episodes"):
        vis_key = (ep.suite, ep.task)
        cur_vis_count = visualization_counts.get(vis_key, 0)
        save_visualization = args.vis_num_demos_per_task > 0 and cur_vis_count < args.vis_num_demos_per_task
        if save_visualization:
            visualization_counts[vis_key] = cur_vis_count + 1

        row = process_episode(
            ep,
            output_root=output_root,
            camera_keys=tuple(args.camera_keys),
            frame_stride=args.frame_stride,
            image_size=args.image_size,
            bbox_annotator=bbox_annotator,
            overwrite=args.overwrite,
            save_visualization=save_visualization,
            visualization_root=visualization_root,
            visualization_fps=args.vis_fps,
            visualization_max_frames=args.vis_max_frames,
            write_frame_hdf5=args.write_frame_hdf5,
            frame_hdf5_root=Path(args.frame_hdf5_root),
        )
        write_manifest_row(manifest_path, row)

    print(f"[BBOX] done. Manifest: {manifest_path}")


if __name__ == "__main__":
    main()
