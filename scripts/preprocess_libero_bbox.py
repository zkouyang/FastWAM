#!/usr/bin/env python3
"""Offline bbox-label preprocessing for the LIBERO FastWAM dataset.

This bbox-only preprocessor keeps the ATM bbox label behavior, but all runtime
dependencies it needs are local to this FastWAM repository:

1. Task prompts come from `scripts/atm_bbox_prompts.py`.
2. Bboxes are generated by the local torch-2.7-patched GroundingDINO copy under
   `third_party/grounding_dino_torch27`.
3. The default `grounded_sam2` backend grounds objects with GroundingDINO and
   tracks their instance masks through the video with SAM 2.1.

Pass `--bbox-backend grounding_dino` to retain the original per-frame bbox-only
behavior. The default backend additionally saves a `bbox_masks` array of shape
`[K, H, W]`; its first dimension has exactly the same ordering as `bbox_xyxy`
and `bbox_confidences`.

The produced `.npz` cache stores episode-level ragged bbox labels with offsets:

    bbox_xyxy: [K, 4]
    bbox_confidences: [K]
    bbox_offsets: [C, T + 1]

For camera `c` and labeled frame index `t`, use:

    start = bbox_offsets[c, t]
    end = bbox_offsets[c, t + 1]
    boxes = bbox_xyxy[start:end]
    scores = bbox_confidences[start:end]

Optionally pass `--write-frame-hdf5` to also write ATM-style per-frame hdf5
files containing datasets named `bboxes` and `confidences`.
"""

